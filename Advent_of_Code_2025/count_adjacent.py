"""
The problem: Given a matrix of . and @, count the number of @s that are adjacent
to no more than 3 @s in the 8 surrounding spots.

The solution:
1. Create a count matrix (CM) of 0s that is two rows+columns larger than the
original (so that the original matrix is fully embedded)
2. Traverse the original matrix.
3. Anytime we encounter an @, increment the surrounding 8 spots by 1 in the CM
4. Traverse the CM (without the outer rows/columns) and count the number of
spots < 3

For Part 2:
If an @ is counted, it can be removed and the process repeats via recursion..
"""

def count_adjacent(input):
    matrix = input.split('\n')
    matrix_rows_count = len(matrix)
    matrix_columns_count = len(matrix[0])
    count_matrix = [[0] * (matrix_columns_count+2) for _ in range(matrix_rows_count+2)]

    for i in range(matrix_rows_count):
        for j in range(matrix_columns_count):
            if matrix[i][j] == "@":
                for x in range(0,3):
                    for y in range(0,3):
                        if x == 1 and y == 1:
                            continue
                        count_matrix[i+x][j+y] += 1

    total_count = 0
    for i in range(matrix_rows_count):
        for j in range(matrix_columns_count):
            if count_matrix[i+1][j+1] < 4 and matrix[i][j] == "@":
                total_count += 1

    return total_count

def count_adjacent_removed(total_count, matrix):
    print(matrix)
    matrix_rows_count = len(matrix)
    matrix_columns_count = len(matrix[0])
    count_matrix = [[0] * (matrix_columns_count+2) for _ in range(matrix_rows_count+2)]
    temp_count = 0
    for i in range(matrix_rows_count):
        for j in range(matrix_columns_count):
            if matrix[i][j] == "@":
                for x in range(0,3):
                    for y in range(0,3):
                        if x == 1 and y == 1:
                            continue
                        count_matrix[i+x][j+y] += 1

    for i in range(matrix_rows_count):
        for j in range(matrix_columns_count):
            if count_matrix[i+1][j+1] < 4 and matrix[i][j] == "@":
                temp_count += 1
                matrix[i] = matrix[i][:j]+"."+matrix[i][j+1:]

    print(temp_count)
    if temp_count == 0:
        return total_count
    return count_adjacent_removed(total_count+temp_count, matrix)

def implement_recursion(input):
    matrix = input.split('\n')
    return count_adjacent_removed(0, matrix)


